function Product(
	_tenSP,
	_gia,
	_screen,
	_backCamera,
	_frontCamera,
	_hinhAnh,
	_moTa,
	_loaiSP
) {
	this.name = _tenSP;
	this.price = _giaSP;
	this.screen = _screen;
	this.backCamera = _backCamera;
	this.frontCamera = _frontCamera;
	this.img = _hinhAnh;
	this.desc = _moTa;
	this.type = _loaiSP;
}
